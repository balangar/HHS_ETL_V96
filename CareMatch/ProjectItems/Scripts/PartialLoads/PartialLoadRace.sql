USE SACWISCUY
GO

DELETE FROM SACWISCUY.DBO.RACE
WHERE RACE_ID IN
(SELECT *
FROM OPENQUERY(ADHOC_SACRPT,'
SELECT 
	RACE_ID
FROM SACWIS.RACE
WHERE MODIFIED_DATE >= SYSDATE - 7
'))

INSERT INTO SACWISCUY.DBO.RACE
SELECT 
RACE_ID
, PERSON_ID
, RACE_CODE
, CREATED_BY
, CREATED_DATE
, MODIFIED_BY
, MODIFIED_DATE
, APP_VERSION_NBR

FROM OPENQUERY(ADHOC_SACRPT,'
SELECT
RACE_ID
, PERSON_ID
, RACE_CODE
, CREATED_BY
, CREATED_DATE
, MODIFIED_BY
, MODIFIED_DATE
, APP_VERSION_NBR

FROM SACWIS.RACE
WHERE MODIFIED_DATE >= SYSDATE - 7
')